package typecasting;

public class explicit {

	public static void main(String[] args) {
		double a =67.23;
				System.out.println("double value:"+a);
		long b = (long) a;
				System.out.println("long value:"+b);
		int c = (int) b;
		     System.out.println("int value:"+c);
		     
		char d= (char) c;
             System.out.println("char value: " +d);
	
	}


}
