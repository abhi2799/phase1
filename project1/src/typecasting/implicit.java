package typecasting;

public class implicit {
	public static void main(String[] args) {
		byte p=12;
		System.out.println("byte value:" +p);
		int q=p;
		System.out.println("int value:" +q);
		long r=q;
		System.out.println("long value:" +r);
		double s=r;
		System.out.println("double value:"+s);
	}


}
